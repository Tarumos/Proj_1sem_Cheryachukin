a = 15


def square_perimeter(side=a):
    """
    Функция рассчитывает периметр квадрата

    Введите сторону квадрата или будет значение по умолчанию
    """
    return f'Периметр квадрата: {side * 4}'


def square_area(side=a):
    """
    Функция рассчитывает площадь квадрата

    Введите сторону квадрата или будет значение по умолчанию
    """
    return f'Площадь квадрата: {side * side}'
